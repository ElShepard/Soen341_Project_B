public class Position {
    int line;
    int column;

    public Position() {
        this.line = 0;
        this.column = 0;
    }
    public int getLine() {
        return line;
    }
    public void setLine(int line){
        this.line = line;
    }
    public int getColumn() {
        return column;
    }
    public void setCol(int column) {
        this.column = column;
    }
}
